package CoreJavaTraining_Day1;

public class For_Loop{

	public static void main(String[] args) {
		
		
		for(int num1 =1; num1 <= 10; num1 ++)  //This will print in incremental order from 1 to 10
		{
			System.out.println(num1);
		}
		
		for(int num2 =19; num2 <= 26; num2 ++) //this will print 19 to 26
		{
			System.out.println(num2);
		}
		
		for(int num1 =1; num1 <= 10; num1 ++)
		{
			System.out.println(num1);
		}
		
		for(int num3 =20; num3 >0; num3--)  // this will print in decremental order from 20 to 1
		{
			System.out.println(num3);
		}

		
		for (int i = -10; i <= 15; i++) // This will print from negative 10 and add 1 each time until it gets to positive 15
		{
			System.out.println(i);
		}
		
		
		
		for(int num3 =100; num3 >=0; num3--)  // this will print in decremental order from 100 to 0
		{
			System.out.println(num3);
		}
		
		for(int num3 =-50; num3 <=-30; num3++)  // this will print in incremental order with a negative value from from -50 to -30
		{
			System.out.println(num3);
		}
		
		
		
		
		
	} 

}
